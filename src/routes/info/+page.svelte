<h1 class="text-5xl">Info för matkortet</h1>
<br>
<p class="text-gray-800 p-8  text-2xl">Matkortet på NTI Johanneberg gäller för alla elever i årskurs 3. Varje skoldag mellan kl. 10.30-13.30 kan eleverna använda matkortet. Kortet har 75 kr och eleverna har massvis med affärer och restauranger att välja mellan. Observera att matkortet endast fungerar på den nedanstående listan av restauranger. Dessutom får eleverna studentrabbater på många av dessa matställen så att deras 75 kr kan räcka. Om pengarna inte används så sparas dom inte utan försvinner och eleverna kan spendera 75 kr nästa dag. </p>